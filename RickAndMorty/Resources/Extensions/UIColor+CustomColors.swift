import Foundation
import UIKit

extension UIColor {
    static var ramBlue: UIColor {
        return UIColor(named: "RaMBlue")!
    }
    
    static var ramLightBlue: UIColor {
        return UIColor(named: "RaMLightBlue")!
    }
    
    static var ramGreen: UIColor {
        return UIColor(named: "RaMGreen")!
    }
}
